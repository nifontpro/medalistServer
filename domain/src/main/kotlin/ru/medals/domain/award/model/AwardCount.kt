package ru.medals.domain.award.model

/**
 * Тип для сбора статистики по количеству наград в компании
 * по периоду номинации (время начала - конца номинации)
 */
data class AwardCount(
	val before: Long = 0, // До периода номинации
	val nominee: Long = 0, // Во время номинации
	val after: Long = 0, // Номинация закончилась
	val total: Long = 0, // Всего наград в компании
	val error: Long = 0, // Ошибка дат номинации, если время начала больше времени конца
)
